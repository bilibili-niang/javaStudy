package com.itheima.chap03;

public class DataTypesDemo {
    public static void main(String[] args) {
        byte a = 127;
        System.out.println("a:");
        System.out.println(a);
        float aFloat = 1.2344F;
        System.out.println("aFloat:");
        System.out.println(aFloat);
        char aChar = 's';
        System.out.println("aChar:");
        System.out.println(aChar);

    }
}